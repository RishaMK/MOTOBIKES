// UserData.js
import mongoose from 'mongoose';

const UserDataSchema = mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
    },
    isAdmin: {
        type: Boolean,
        default: false,
    },
});

export const User = mongoose.model('User', UserDataSchema);
